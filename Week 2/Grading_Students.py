def gradingStudents(grades):
    # Write your code here
    temp = []
    for i in grades:
        if (i <= 37 or (i % 5 <= 2)):
            temp.append(i)
        else:
            k = i % 5
            if k>=3:
                temp.append(i + (5-k))
            else:
                temp.append(i)

    return temp

